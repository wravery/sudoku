export const remainingValues = derived<
  [typeof selected, typeof current],
  Promise<number[]> | null
>([selected, current], ([$selected, $current]) => {
  if (!$selected) {
    return null;
  }
  return invoke<number[]>("get_possible_values", {
    board: $current,
    row: $selected.row,
    column: $selected.column,
  });
});

